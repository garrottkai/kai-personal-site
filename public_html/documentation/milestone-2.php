			<h2>Interaction Flow</h2>
			<p>The user (Allison, in this case, on her 27-inch work computer) will navigate to the home page and be presented with a very simple, refined layout. Upon reading the introduction, she will click on the "Profile" link and click on the first item. A large image of the selected site will load. She will examine it, then exit the enlarged view, click the arrow to the side and look at the next entry. After determining that my work is satisfactory, she will navigate to the last page, "About Me," and read the paragraph found there. Then, she will open my social media profiles from the conveniently located buttons, and save my email address.</p>
